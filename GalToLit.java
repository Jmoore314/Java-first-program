// converts gallons to liters
public class GalToLit {
  public static void main(String[] args){
    double gallons;
    double liters;

    gallons = 10;

    liters = gallons * 3.7854; // the conversion of gallons to liters

    System.out.println(gallons + " gallons is " + liters + " liters.");
  }  
}